int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 0)
		nb = -nb;
	while (nb % i != 0)
	{
		if (i > nb)
			return (0);
		i++;
	}
	if (i == nb)
		return (1);
	else
		return (0);
}
